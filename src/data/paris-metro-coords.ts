function haversine(lat1: number, lon1: number, lat2: number, lon2: number): number {
  const R = 6371000;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLon = ((lon2 - lon1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLon / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

/** Return the N nearest stations to the given point, each with walking-time estimate. */
export function findNearestStations(
  lat: number,
  lon: number,
  count = 2,
): { name: string; distanceMeters: number; minutes: number }[] {
  return PARIS_METRO_STATIONS
    .map((s) => {
      const d = haversine(lat, lon, s.lat, s.lon);
      return { name: s.name, distanceMeters: d, minutes: Math.max(1, Math.round(d / 80)) };
    })
    .sort((a, b) => a.distanceMeters - b.distanceMeters)
    .slice(0, count);
}
